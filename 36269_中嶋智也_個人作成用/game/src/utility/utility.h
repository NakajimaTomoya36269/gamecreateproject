///============================================================
/// 度をラジアンに変換するマクロ
/// 角度(degree)をラジアン(radian)に変換する
/// 使用例:
///     float rad = DEG_TO_RAD(90.0f); // 90度 → π/2 ラジアン
///============================================================
#define DEG_TO_RAD(deg)		((deg) * 3.1415f / 180.0f)
