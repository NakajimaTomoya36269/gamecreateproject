#pragma once

//==================================================
// ゲームメイン状態ID
//==================================================
// Gamemain シーン内での状態遷移を表す識別子。
// ・PLAY  : 通常のゲーム進行状態
// ・PAUSE : ゲーム進行を停止し、入力待ちとなる状態
//
// enum class を使用することで
// ・スコープ汚染を防止
// ・型安全な状態管理を実現
//==================================================
enum class GAMEMAIN_STATE_ID
{
	PLAY,	// プレイ状態（通常進行）
	PAUSE,	// ポーズ状態
};
