#pragma once

#include "../scene.h"
#include "vivid.h"

//-----------------------------------------------------------------------------
// タイトルシーンクラス
//-----------------------------------------------------------------------------
// ・ゲーム開始前の演出専用シーン
// ・入力受付とシーン遷移のみを担当
// ・ゲームロジックは一切持たない
//-----------------------------------------------------------------------------
class CTitle
	: public IScene
{
public:
	//-------------------------------------------------------------------------
	// コンストラクタ / デストラクタ
	//-------------------------------------------------------------------------

	// 軽量初期化のみを行う
	CTitle(void);

	// 明示的なリソース解放を持たないため default
	~CTitle(void) = default;

	//-------------------------------------------------------------------------
	// IScene インターフェース
	//-------------------------------------------------------------------------

	// フォント生成・演出パラメータ初期化
	void Initialize(void) override;

	// 演出更新・入力受付
	void Update(void) override;

	// 背景・タイトル・UI 描画
	void Draw(void) override;

	// 後処理（現状なし）
	void Finalize(void) override;

private:
	//-------------------------------------------------------------------------
	// 定数パラメータ
	//-------------------------------------------------------------------------

	// デバッグ表示用フォントサイズ
	static const int	m_font_size;

	// ENTER 表示用フォントサイズ
	static const int	m_enter_font_size;

	//-------------------------------------------------------------------------
	// 表示・演出用メンバ
	//-------------------------------------------------------------------------

	// デバッグ表示位置
	vivid::Vector2		m_Position;

	// ENTER 表示位置
	vivid::Vector2		m_EnterPosition;

	// アルファアニメーション用角度（sin 波）
	float				m_Angle;

	// ENTER 表示用カラー（ARGB）
	unsigned int		m_Color;
};
