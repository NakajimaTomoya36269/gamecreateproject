#pragma once

//-----------------------------------------------------------------------------
// シーン基底インターフェース
//-----------------------------------------------------------------------------
// ・Title / Gamemain / GameClear / GameOver など
//   すべてのシーンが共通で持つライフサイクル定義
// ・SceneManager から多態的に扱うための基底クラス
//-----------------------------------------------------------------------------
class IScene
{
public:
	//-------------------------------------------------------------------------
	// コンストラクタ / デストラクタ
	//-------------------------------------------------------------------------

	// 派生クラスでの初期化を前提とするため軽量
	IScene(void);

	// 派生クラスを正しく破棄するため virtual
	virtual ~IScene(void);

	//-------------------------------------------------------------------------
	// ライフサイクル関数
	//-------------------------------------------------------------------------

	// シーン開始時に一度だけ呼ばれる
	// ・リソース生成や状態初期化を行う
	virtual void Initialize(void);

	// 毎フレーム呼ばれる更新処理
	// ・入力処理、状態遷移、ゲームロジック更新など
	virtual void Update(void);

	// 毎フレーム呼ばれる描画処理
	// ・描画順は SceneManager 側で制御
	virtual void Draw(void);

	// シーン終了時に呼ばれる
	// ・Initialize() と対になる後処理
	virtual void Finalize(void);
};
