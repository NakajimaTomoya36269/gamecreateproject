#pragma once

//==================================================
// シーンID定義
//==================================================
// SceneManager が現在のシーンおよび遷移先を
// 識別するための列挙型。
//==================================================
//
// enum class を使用することで
// ・グローバル名前空間の汚染を防止
// ・型安全なシーン管理を実現
//==================================================
enum class SCENE_ID
{
	DAMMY,		// 初期状態用ダミーシーン
	TITLE,		// タイトルシーン
	GAMEMAIN,	// ゲームメインシーン
	GAMEOVER,	// ゲームオーバーシーン
	GAMECLEAR,	// ゲームクリアシーン
};
