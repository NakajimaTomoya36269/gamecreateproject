#pragma once

#include "vivid.h"
#include "../stage.h"

//====================================================
// ロング床クラス
//====================================================
// ・横に長い通常床
// ・基本的には「動かない」「落ちない」床
// ・ステージの地面・足場として使用
//====================================================
class CLongFloor
	: public IStage
{
public:
	//================================================
	// コンストラクタ
	//================================================
	// ・床サイズと STAGE_ID は cpp 側で基底に設定
	CLongFloor(void);

	// デストラクタ
	~CLongFloor(void) = default;

	//================================================
	// 初期化
	//================================================
	// @param position : ステージ上での配置位置
	// ・基底クラスの初期化処理を呼び出す
	void Initialize(const vivid::Vector2& position) override;

	//================================================
	// 描画
	//================================================
	// ・ロング床専用テクスチャを描画
	// ・色指定はデバッグ・演出用
	void Draw(void) override;

private:
	//================================================
	// 定数（クラス共通）
	//================================================
	static const int m_width;	// 床の横幅
	static const int m_height;	// 床の高さ
	static const std::string m_floor_texture_path;	// 床の絵のファイルパス
};
