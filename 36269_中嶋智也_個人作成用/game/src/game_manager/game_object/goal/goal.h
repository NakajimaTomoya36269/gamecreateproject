#pragma once

#include "vivid.h"

//==================================================
// ゴールクラス
// ・ステージクリア判定用のオブジェクト
// ・スクロールに合わせて移動する
//==================================================
class CGoal
{
public:
	//==================================================
	// コンストラクタ / デストラクタ
	//==================================================
	CGoal(void);
	~CGoal(void) = default;

	//==================================================
	// 初期化
	// ・ゴールの初期位置を設定
	//==================================================
	void Initialize(const vivid::Vector2& position);

	//==================================================
	// 更新
	// ・スクロールに応じた移動処理
	//==================================================
	void Update(void);

	//==================================================
	// 描画
	//==================================================
	void Draw(void);

	//==================================================
	// 解放
	// ・現在は特に処理なし
	//==================================================
	void Finalize(void);

	//==================================================
	// サイズ取得
	//==================================================
	int GetWidth(void);
	int GetHeight(void);

	//==================================================
	// 位置取得
	//==================================================
	vivid::Vector2 GetPosition(void);

private:
	//==================================================
	// 定数
	//==================================================
	static const int			m_width;		// ゴールの横幅
	static const int			m_height;		// ゴールの縦幅
	static const float			m_scroll_speed;	// スクロール移動速度
	static const float			m_friction;		// 摩擦（減速率）
	static const std::string	m_goal_texture_path;	// ゴールの絵のファイルパス

	//==================================================
	// 変数
	//==================================================
	vivid::Vector2				m_Position;		// 位置
	vivid::Vector2				m_Velocity;		// 速度
};
