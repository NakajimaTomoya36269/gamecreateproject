#pragma once

#include "vivid.h"
#include "../item.h"

//==================================================
// 無敵アイテムクラス
//==================================================
// IItem を継承し、固定パラメータを持つ取得型アイテム。
// 振る舞いは基底クラスに委譲し、本クラスは描画責務のみを持つ。
class CInvincibleItem
	: public IItem
{
public:
	//==================================================
	// コンストラクタ / デストラクタ
	//==================================================

	// 固定サイズ・当たり判定・ID は cpp 側で定義
	CInvincibleItem(void);

	// 明示的なリソースを保持しないため default
	~CInvincibleItem(void) = default;

	//==================================================
	// 描画
	//==================================================

	// IItem インターフェース実装
	void Draw(void) override;

private:
	//==================================================
	// クラス共通パラメータ
	//==================================================

	// 描画・当たり判定サイズ
	static const int   m_width;
	static const int   m_height;

	// 円形当たり判定用半径
	static const float m_radius;
};
