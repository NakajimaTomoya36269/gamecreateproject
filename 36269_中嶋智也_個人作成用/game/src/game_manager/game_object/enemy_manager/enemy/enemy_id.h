/*
==================================================
 敵識別ID
 ・敵の種類を識別するためのID
 ・生成処理や挙動の分岐で使用
==================================================
*/
enum class ENEMY_ID
{
	ENEMYA,	// 敵A：基本的な移動型（上下移動など）
	ENEMYB,	// 敵B：床判定あり・逆重力系
	ENEMYC,	// 敵C：プレイヤーを狙って弾を撃つ
	ENEMYD,	// 敵D：上下方向に弾を撃つ固定砲台型
};
